package Switch;

public class Problem2 {
    public static void main(String[] args) {
        String month = "january";
        switch (month)
        {
            case "january": System.out.println("January");break;
            case "february": System.out.println("February");break;
            case "march":System.out.println("March");break;
            case "april": System.out.println("April");break;
            case "may": System.out.println("April");break;
            case "june": System.out.println("April");break;
            case "july": System.out.println("April");break;
            case "august": System.out.println("April");break;
            case "september": System.out.println("April");break;
            case "october": System.out.println("April");break;
            case "november": System.out.println("April");break;
            case "december": System.out.println("April");break;
            default:
                System.out.println("not a month");

        }
    }
}
